#include <iostream>
using namespace std;
int main()
{
    int a, b;

    cin >> a >> b;

    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;


    double s, l;

    cin >> s >> l;

    cout << "s + l = " << s + l << endl;
    cout << "s - l = " << s - l << endl;
    cout << "s * l = " << s * l << endl;
    cout << "s / l = " << s / l;






}



